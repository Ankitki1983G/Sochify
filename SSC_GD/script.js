function check(btn, sel, ans){
  clearInterval(timer);
  document.querySelectorAll(".option").forEach(b => b.disabled = true);

  if(sel === ans){
    btn.classList.add("btn-success");
    score += 10;
  } else {
    btn.classList.add("btn-danger");
    showCorrectAnswer(ans);
  }

  scoreEl.textContent = score;
  setTimeout(() => {
    index++;
    load();
  }, 1500);
}

function showCorrectAnswer(ans){
  document.querySelectorAll(".option").forEach(b => {
    if(b.textContent === ans) b.classList.add("btn-success");
    b.disabled = true;
  });
}

// saveScore
function saveScore(){
  fetch("leaderboard.php", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      name: nameInput.value.trim(),
      score: score
    })
  }).catch(err => console.log("Save error:", err));
}

function loadLeaderboard(){
  fetch("leaderboard.php")
    .then(res => res.json())
    .then(data => {
      const box = document.getElementById("leaderboard");
      box.innerHTML = "";

      data.forEach((u,i) => {
        const div = document.createElement("div");
        div.className = "lb-entry";
        div.style.animation = "fadeInUp 0.4s forwards";
        div.style.animationDelay = (i * 0.1) + "s";
        div.innerHTML = `<b>${i+1}.</b> ${u.name} — ${u.score}`;
        box.appendChild(div);
      });
    })
    .catch(err => console.log("Load error:", err));
}


/* ---------------- END ---------------- */
function end(){
  quiz.classList.add("d-none");
  result.classList.remove("d-none");
  finalEl.innerHTML = `<h4 class="text-center text-success">Your Score: ${score}</h4>`;

  saveScore();
  loadLeaderboard();
}

