/* ---------------- SHUFFLE ---------------- */
function shuffle(arr){
  let a = [...arr];
  for(let i = a.length - 1; i > 0; i--){
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function getStoredIndexes(level){
  return JSON.parse(localStorage.getItem("used_" + level) || "[]");
}

function storeIndexes(level, indexes){
  localStorage.setItem("used_" + level, JSON.stringify(indexes));
}

/* ---------------- START ---------------- */
startBtn.onclick = () => {
  const name = nameInput.value.trim();
  const level = levelSelect.value;

  if(!name || !level){
    alert("नाम और Level चुनो!");
    return;
  }

  const filtered = bank
    .map((q,i)=>({...q, idx:i}))
    .filter(q => q.level === level);

  if(filtered.length < 20){
    alert("इस level में कम से कम 20 सवाल होने चाहिए!");
    return;
  }

  let used = getStoredIndexes(level);
  let fresh = filtered.filter(q => !used.includes(q.idx));

  if(fresh.length < 20){
    used = [];
    fresh = filtered;
  }

  const selected = shuffle(fresh).slice(0,20);
  const selectedIndexes = selected.map(q => q.idx);

  used = [...used, ...selectedIndexes];
  storeIndexes(level, used);

  questions = selected.map(q => {
    const {idx, ...rest} = q;
    return rest;
  });

  index = 0;
  score = 0;
  scoreEl.textContent = 0;

  login.classList.add("d-none");
  quiz.classList.remove("d-none");
  load();
};


/* ---------------- LOAD ---------------- */
function load(){
  if(index >= questions.length){
    end();
    return;
  }

  const q = questions[index];
  questionEl.textContent = `Q${index + 1}. ${q.q}`;
  categoryEl.textContent = "Category: " + q.cat;

  optionsEl.innerHTML = "";
  q.options.forEach(opt => {
    const b = document.createElement("button");
    b.className = "option btn btn-outline-primary";
    b.textContent = opt;
    b.onclick = () => check(b, opt, q.answer);
    optionsEl.appendChild(b);
  });

  time = 10;
  bar.style.width = "100%";
  timerEl.textContent = "10s";

  clearInterval(timer);
  timer = setInterval(() => {
    time--;
    timerEl.textContent = time + "s";
    bar.style.width = (time * 10) + "%";

    if(time <= 0){
      clearInterval(timer);
      showCorrectAnswer(q.answer);
      setTimeout(() => {
        index++;
        load();
      }, 1500);
    }
  }, 1000);
}

