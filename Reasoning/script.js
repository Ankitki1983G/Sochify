// Pick 10 questions randomly (repetition allowed)
function pickRandomQuestionsWithRepetition() {
    let selected = [];
    for (let i = 0; i < 10; i++) {
        const randomIndex = Math.floor(Math.random() * questionBank.length);
        selected.push(questionBank[randomIndex]);
    }
    return selected;
}

